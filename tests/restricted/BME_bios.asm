		LD	(CY),A

		LD	A,(WIDTH)
		ADD	A,' '
		LD	C,A
		INC	DE
		LD	A,(DE)		;GET X
		CP	' '
		JR	NC,LOCATE4
		LD	A,' '
LOCATE4:	CP	C
		JR	C,LOCATE5
		DEC	C
		LD	A,C
LOCATE5:	SUB	' '
		LD	(CX),A
		JP	CSR

