		SUB	01CH-09
		JP	TRNC01

;	ROLL UP
ROLUP:		LD	A,6
		JP	TRNC01

;	ROLL DOWN
ROLDWN:		LD	A,7
		JP	TRNC01

;	ESCAPE SEQ

ESCAPE:		LD	A,-1
		LD	(PRMODE),A	;SET ESC MODE
		RET

;	ESCAPE COMMAND ENTRY
ESCCMD:		LD	HL,ESCCMDTBL
ESCCMDL:	LD	A,(HL)
		OR	A
		JP	Z,ESCCMD2	;IF UNDEFINE SEQ
		CP	B
		JP	Z,ESCCMD3	;IF DEFINE SEQ
		LD	DE,4
		ADD	HL,DE
		JR	ESCCMDL		;JP NEXT SEQ

;	UNDEFINE SEQ
ESCCMD2:	LD	(PRMODE),A
		LD	A,B
		JP	PRINT

;	DEFINE SEQ ENTRY
ESCCMD3:	INC	HL
		LD	A,(HL)
		OR	A
		JR	Z,ESCCMD4	;IF NON PARARMETER SEQ
		LD	(PRMODE),A
		INC	HL
		LD	E,(HL)
		INC	HL
		LD	D,(HL)
		LD	(CMDJP+1),DE	;SET ENTRY POINT
		RET

;	NON PARAMETER SEQ
ESCCMD4:	XOR	A
		LD	(PRMODE),A
		INC	HL
		LD	E,(HL)
		INC	HL
		LD	D,(HL)
		EX	DE,HL
		JP	(HL)

;	ESCAPE PARAMETER SET ENTRY
ESCPRM:		LD	HL,ESCPRMTOP
		LD	C,A
		LD	A,(PRMCOUNT)
		LD	E,A
		LD	D,0
		LD	A,C
		ADD	HL,DE
		LD	(HL),B
		LD	HL,PRMODE
		DEC	(HL)
		JR	NZ,ESCPRM2
		XOR	A		;IF LAST PARAMETER
		LD	(PRMCOUNT),A
		LD	DE,ESCPRMTOP
CMDJP:		JP	00000

ESCPRM2:	LD	HL,PRMCOUNT
		INC	(HL)
		RET

;	ESCAPE COMMAND TABLE
ESCCMDTBL:	DB	'T',0		;ERASE END OF LINE
		DW	ESCT
		
		DB	'Y',0		;ERASE END OF SCREEN
		DW	ESCY
		
		DB	'*',0		;CLS
		DW	ESCSTAR
		
		DB	'(',0		;REVERSE
		DW	REVERSE

		DB	')',0		;NORMAL
		DW	NORMAL
		
		DB	'C',0		;CURSOR ON/OFF
		DW	ESCC
		
		DB	'_',1		;CURSOR ON/OFF
		DW	CURSOR

		DB	'<',0		;WIDTH 40
		DW	WID40
		
		DB	'>',0		;WIDTH 80
		DW	WID80
		
		DB	'S',1		;SCREEN
		DW	SCREEN

		DB	'{',0		;BEEPON
		DW	BEEPON
		
		DB	'}',0		;BEEP OFF
		DW	BEEPOFF
		
		DB	'=',2		;LOCATE
		DW	LOCATE
		
		DB	'M',1		;CHARACTER COLOR
		DW	ESCM
		
		DB	'B',1		;BACK GROUND COLOR
		DW	ESCB
		
		DB	'F',1		;FRAME COLOR
		DW	ESCF

		DB	'c',3		;CHAR,BACK,FRAME COLOR
		DW	CHRCOL
		
		DB	'D',1		;DISPLAY PLANE
		DW	DSPSCRN

		DB	'W',1		;WRITE PLANE
		DW	WRTSCRN

		DB	'K',2		;KANJI PRINT
		DW	KANJI

		DB	'N',1
		DW	NIPMODE

		DB	'L',1
		DW	KNJLINE

		DB	'^',0
		DW	STATMODE

	IF	JISHO
		DB	'J',1
		DW	STUDYMODE
	ENDIF

		DB	'\',0		;BACK SLASH SW.
		DW	BKSLASH
		
		DB	0		;END MARK

PRMCOUNT:	DB	0



