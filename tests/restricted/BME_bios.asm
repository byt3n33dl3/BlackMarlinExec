ESCF:		LD	A,(DE)
		AND	07H
		LD	B,A
		LD	HL,XCOL
		LD	A,(HL)
		AND	70H
		OR	B
		LD	(HL),A
		INC	HL
		INC	HL
		LD	A,M
		AND	70H
		OR	B
		LD	(HL),A
		JP	NORMAL

CHRCOL:		INC	DE
		CALL	ESCB
		DEC	DE
		CALL	ESCM
		INC	DE
		INC	DE
		JR	ESCF

