		DW	LPT0

;	LIST STATUS ROUTINE
LISTST:		LD	A,(IOBYT)
		RLCA
		RLCA
		LD	HL,LISTSTTB
		JR	IOJP
	
LISTSTTB: 	DW	TTYOUTST
		DW	TTYOUTST
		DW	LPT0ST
		DW	LPT0ST

IOJP:		AND	03
		ADD	A,A
		LD	E,A
		LD	D,0
		ADD	HL,DE
		LD	A,(HL)
		INC	HL
		LD	H,(HL)
		LD	L,A
		JP	(HL)
	
;***** TTY IO ROTINE *************


CONIN:
READER:
TTYIN:		LD	(BDOSSP),SP
		LD	SP,BIOSSP
TTYIN1B:	CALL	BLNKON
TTYIN1:		LD	A,(KANJIMODE)
		OR	A
		JR	Z,TTYIN15
		LD	A,(KINPMOD)
		OR	A
        	JR	NZ,KTTYIN
TTYIN15:	DI
        	CALL	GETRNG
		EI
		JR	Z,TTYIN1 
TTYIN1A:	PUSH	AF
		LD	A,(BLNKFLG)
		OR	A
		CALL	Z,BLNKOFF
TTYIN2:		POP	AF
		JR	BIOSRET

KTTYIN:
		CALL	KGETRNG
		JR	NZ,TTYIN1A
KTTYINB:	JP	KNJINP0

CONOUT:
TTYOUT:		LD	(BDOSSP),SP
		LD	SP,BIOSSP
		LD	A,C
		CALL	PRINT
		JR	BIOSRET

TTYOUTST:
LPT0ST: 	LD	A,0FFH
		RET

;*******LIST I/O ROUTINE ******

