		OR	B
		LD	(XCOL1),A
		POP	AF
		RLCA
		RLCA
		RLCA
		RLCA
		LD	(XCOL1+1),A
		AND	70H
		LD	B,A
		DEC	HL
		LD	A,(HL)
		AND	07H
		OR	B
		LD	(HL),A
		JP	NORMAL
		
