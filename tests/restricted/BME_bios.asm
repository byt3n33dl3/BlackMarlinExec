		AND	03H
		CP	1		;IF A=1 THEN 
		JR	Z,SCREEN1	;       SCREEN 1
		CP	3		;ELSE IF A=3 THEN
		JR	Z,SCREEN3	;       SCREEN 3
					;ELSE   SCREEN 0
SCREEN0:	LD	A,(SCREENM)
		AND	A
		RET	Z
		XOR	A
		LD	(KANJIMODE),A
		JR	SCREENC

