;***** TRANSCMD *****

;	IN: A:COMAND CODE

CMD:		PUSH	BC
		LD	(CMDDT),A
CMDLOP:		DI
		LD	BC,CMDINT
		LD	(INT87),BC
		LD	BC,PORT
		OUT	(C),A
		CALL	INTON
		EI
		HALT
		POP	BC
		OR	A
		RET	Z
		SCF
		CALL	KEYANAL
		LD	A,(CMDDT)
		JR	CMD

CMDINT:		LD	BC,PORT
		IN	A,(C)
		RET


;***** PUTDATA *****

;	IN: A:DATA

PUTDATA:	PUSH	BC
		LD	BC,PUTINT
		LD	(INT87),BC
		LD	BC,PORT
		CALL	INTON
		OUT	(C),A
PUTDATA1: 	EI
		HALT
		POP	BC
		RET
	
PUTINT:		IN	A,(C)
		RET
	
;***** GETDATA *****

;	OUT:A:DATA

GETDATA:	PUSH	BC
		LD	BC,GETINT
		LD	(INT87),BC
		EI
		HALT
		POP	BC
		RET
	
GETINT:		LD	BC,PORT
		IN	A,(C)
		PUSH	AF
		XOR	A
		OUT	(C),A
		POP	AF
		JP	INTON

	
;***** KEYINT *****

KEYSP		EQU	0FE00H

KEYINT:		LD	(KEYSPBK),SP
		LD	SP,KEYSP
		PUSH	AF
		PUSH	BC
		PUSH	DE
		PUSH	HL
		LD	BC,PORT
		IN	A,(C)
		OR	A
		CALL	KEYANAL
		LD	BC,KEYINT
		LD	(INT87),BC
		LD	A,(INTBACK)
		OR	10H
		LD	(INTBACK),A
		LD	BC,INTPORT
		OUT	(C),A
		POP	HL
		POP	DE
		POP	BC
		POP	AF
		LD	SP,(KEYSPBK)
		EI
		RET
	
;***** KEYANAL *****

;	IN: A:ACKCODE

KEYANAL:	PUSH	BC
		PUSH	DE
		PUSH	HL
		PUSH	AF
		LD	D,0
		CP	1
		JR	NZ,KEYANAL1
		LD	D,2
KEYANAL1: 	LD	BC,PORT
		OUT	(C),D
		POP	AF
		PUSH	AF
		LD	BC,INTPORT
		JR	C,KEYANAL2
		LD	A,90H
		OUT	(C),A
KEYANAL2: 	LD	A,10H
		OUT	(C),A
		POP	AF
		CP	2
		JR	Z,KEYANAL9
		CP	3
		JR	NC,KEYANAL3
		LD	(KEYFLG),A
		JR	KEYANAL9
	
KEYANAL3: 	LD	(KEYFLG),A
		LD	A,10
KEYANAL4: 	DEC	A
		JR	NZ,KEYANAL4
	
		LD	BC,KANALINT
		LD	(INT87),BC
		EI
		HALT
		LD	(KEYDATA),A
		CALL	KEYIN
KEYANAL9:	POP	HL
	 	POP	DE
		POP	BC
		RET

KANALINT: 	LD	BC,PORT
		IN	A,(C)
		LD	E,A
		XOR	A
		OUT	(C),A
		CALL	INTON
		LD	A,E
		RET

INTON:		PUSH	AF
		PUSH	BC
		LD	BC,INTPORT
		LD	A,90H
		OUT	(C),A
		LD	A,10H
		OUT	(C),A
		POP	BC
		POP	AF
		RET


;----- TRNC01 -----
;	IN: A:COMMAND

TRNC01:		CALL	CMD

;***** SUBEND *****

SUBEND:		PUSH	AF
		PUSH	BC
		LD	BC,KEYINT
		LD	(INT87),BC
		LD	BC,INTPORT
		LD	A,(INTBACK)
		OUT	(C),A
		POP	BC
		POP	AF
		EI
		RET
	
;----- TRNC02 -----
;	IN: A:COMMAND
;	    B:DATA

TRNC02:		CALL	CMD
		LD	A,B
		CALL	PUTDATA
		JR	SUBEND

;----- TRNC03 -----
;	IN: A:COMMAND
;	    BC:DATA

TRNC03:		CALL	CMD
		LD	A,B
		CALL	PUTDATA
		LD	A,C
		CALL	PUTDATA
		JR	SUBEND

;---- TRNC04 ----
;	IN: A:COMMAND
;	    B:PARAMETER COONT
;	    HL:PARAMETER POINTER

TRNC04:		CALL	CMD
TRNC04L:	LD	A,(HL)
		CALL	PUTDATA
		INC	HL
		DJNZ	TRNC04L
		
		JR	SUBEND
	
;----- TRNCGET -----
;	IN: A:COMMAND
;	OUT:A:DATA

TRNCGET:	CALL	CMD
		CALL	GETDATA
		JR	SUBEND

;***** LOGICAL PRINT ROUTINE *****

;	IN: A:CODE

PRINT:		LD	B,A
		LD	HL,PRMODE
		LD	A,(HL)
		CP	-1
		JP	Z,ESCCMD
		OR	A
		JP	NZ,ESCPRM
		LD	A,B
		CP	' '		;IF CONTROL CODE
		JP	C,PRCNTCD
		LD	A,(K1STAT)
		OR	A
		JP	NZ,KNJPR

PRND0:		LD	A,(KANJIMODE)
		AND	A
		JR	Z,PRND1B
		LD	A,B
		CP	80H
		JR	C,PRND1
		CP	0E0H
		JR	NC,KNJPRINT
		CP	0A0H
		JR	NC,PRND1

KNJPRINT:	LD	(KCODEH),A
		LD	A,-1
		LD	(K1STAT),A
		LD	B,1CH
		JP	RIGHT

PRND1B:		LD	A,B
PRND1:		CP	7FH
		RET	Z
		CP	0FFH
		RET	Z
PRND1C:		CALL	PRNT
PRND1S:		LD	HL,CX
		INC	(HL)
		LD	A,(WIDTH)
		CP	(HL)
		RET	NZ
PRND1K:		XOR	A
		LD	(HL),A
		LD	HL,CY
		INC	(HL)
		LD	A,(CONSOLE)
		CP	(HL)
		RET	NZ
		DEC	(HL)
		RET
	
PRCNTCD:	LD	A,B
		CP	07
		JR	Z,BELL		;BEEP
		CP	08
		JR	Z,BS		;BACK SPACE
		CP	09
		JR	Z,TAB		;TAB
		CP	0AH
		JR	Z,LF		;LINE FEED
		CP	0BH
		JP	Z,HM		;HOME POSITION
		CP	0CH
		JP	Z,CLS		;CLEAR	CSREEN
		CP	13
		JP	Z,CR		;CARRIAGE RETURN
		CP	1CH
		JP	Z,RIGHT		;CURSOR RIGHT
		CP	1DH
		JP	Z,LEFT		;CURSOL LEFT
		CP	1EH
		JP	Z,UP		;CURSOL UP
		CP	1FH
		JP	Z,DOWN		;CURSOL DOWN
		CP	1
		JP	Z,ROLUP		;ROLL UP
		CP	10H
		JP	Z,ROLDWN	;ROLL DOWN
		CP	27
		JP	Z,ESCAPE	;ESCAPE SEQ
		RET

;	BEEP OUT
BELL:		LD	A,28		;MACRO COMMAND 28
		JP	TRNC01

;	BACK SPACE
BS:		LD	A,(CX)
		OR	A
		JR	NZ,BS1
		LD	A,(CY)
		OR	A
		RET	Z
BS1:		LD	HL,CX
		DEC	(HL)
		LD	A,0FFH
		CP	(HL)
		JR	NZ,BS2
		LD	A,(WIDTH)
		DEC	A
		LD	(HL),A
		LD	HL,CY
		DEC	(HL)
BS2:		LD	A,15		;MACRO COMMAND 15
		JP	TRNC01

;	TAB
TAB:		LD	HL,CX
		LD	A,8
		ADD	A,(HL)
		AND	0F8H
		LD	(HL),A
		LD	A,(WIDTH)
		CP	(HL)
		JR	NZ,TAB1
		LD	(HL),0
		LD	HL,CY
		INC	(HL)
		LD	A,(CONSOLE)
		CP	(HL)
		JR	NZ,TAB1
		DEC	(HL)
		CALL	CRLF
TAB1:		JP	CSR

;	LINE FEED
LF:		LD	HL,CY
		INC	(HL)
		LD	A,(CONSOLE)
		CP	(HL)
		JR	NZ,LF1
		DEC	(HL)
		CALL	CRLF
LF1:		JP	CSR

;	HOME POSITION
HM:		XOR	A
		LD	(CX),A
		LD	(CY),A
		LD	(K1STAT),A
		JP	CSR

;	CLEAR SCREEN
CLS:		LD	A,13		;MACRO COMMAND 13
		CALL	TRNC01
		JR	HM

;	CARRIAGE RETURN
CR:		LD	HL,CX
		LD	(HL),0
		JP	CSR

;	CURSOR RIGHT
RIGHT:		LD	HL,CX
		INC	(HL)
		LD	A,(WIDTH)
		CP	(HL)
		JR	NZ,XCSR
		LD	(HL),0
		LD	A,(CONSOLE)
		DEC	A
		INC	HL
		CP	(HL)
		JR	NZ,DOWN
		LD	A,' '
		JP	PRNT

;	CURSOR LEFT
LEFT:		LD	HL,CX
		DEC	(HL)
		LD	A,-1
		CP	(HL)
		JR	NZ,XCSR
		LD	A,(WIDTH)
		DEC	A
		LD	(HL),A

;	CURSOR UP
UP:		LD	HL,CY
		DEC	(HL)
		LD	A,0FFH
		CP	(HL)
		JR	NZ,XCSR
		LD	A,(CONSOLE)
		DEC	A
		LD	(HL),A
		JR	XCSR

;	CURSOR DOWN
DOWN:		LD	HL,CY
		INC	(HL)
		LD	A,(CONSOLE)
		CP	(HL)
		JR	NZ,XCSR
		LD	(HL),0

